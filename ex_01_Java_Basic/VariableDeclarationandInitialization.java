package ex_01_Java_Basic;

public class VariableDeclarationandInitialization {
    public static void main(String[] args) {
        int a =25;
        double b = 3.14;
        boolean y = true;
        char z = 'A';

        System.out.print("Integer:" + a   );

        System.out.print("Double:" + b );

        System.out.print("Boolean:" + y );

        System.out.print("Character:" + z );
    }
}
